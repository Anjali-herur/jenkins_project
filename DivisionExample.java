public class DivisionExample {
    public static void main(String[] args) {
        double dividend = 10.0;
        double divisor = 4.0;
        
        // Performs exact division
        double quotient = dividend / divisor; 
        
        // Output: 10.0 / 4.0 = 2.5
        System.out.println(dividend + " / " + divisor + " = " + quotient);
    }
}
