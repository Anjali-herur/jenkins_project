public class MultiplyNumbers {
    public static void main(String[] args) {
        // Define two integer numbers
        int num1 = 15;
        int num2 = 4;
        
        // Multiply the numbers
        int product = num1 * num2;
        
        // Display the result
        System.out.println("The product of " + num1 + " and " + num2 + " is: " + product);
    }
}
